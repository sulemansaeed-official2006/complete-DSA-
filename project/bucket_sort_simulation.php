    <script>
        let arr = [];
        const SIZE = 10;
        const BUCKET_COUNT = 5; // 0.0-0.2, 0.2-0.4, etc.
        const DELAY = 800;
        
        const mainArrayDiv = document.getElementById('mainArray');
        const sortedArrayDiv = document.getElementById('sortedArray');
        const bucketsContainer = document.querySelector('.buckets-grid');
        const logDiv = document.getElementById('log');

        // Init Buckets
        for(let i=0; i<BUCKET_COUNT; i++) {
            const rangeStart = (i * (1/BUCKET_COUNT)).toFixed(1);
            const rangeEnd = ((i+1) * (1/BUCKET_COUNT)).toFixed(1);
            
            const bucket = document.createElement('div');
            bucket.className = 'bucket';
            bucket.id = `bucket-${i}`;
            bucket.innerHTML = `<span class="bucket-label">${rangeStart} - ${rangeEnd}</span>`;
            bucketsContainer.appendChild(bucket);
        }

        function generateRandomArray() {
            arr = [];
            for(let i=0; i<SIZE; i++) {
                // Random float 0.00 to 0.99
                arr.push(parseFloat(Math.random().toFixed(2)));
            }
            renderMainArray();
            sortedArrayDiv.innerHTML = ''; // Clear sorted
            clearBuckets();
            addLog('Generated new random floats.', 'info');
        }

        function renderMainArray() {
            mainArrayDiv.innerHTML = '';
            arr.forEach((num, idx) => {
                const el = document.createElement('div');
                el.className = 'num-block';
                el.id = `num-${idx}`;
                el.innerText = num.toFixed(2);
                mainArrayDiv.appendChild(el);
            });
        }

        function clearBuckets() {
            for(let i=0; i<BUCKET_COUNT; i++) {
                const rangeStart = (i * (1/BUCKET_COUNT)).toFixed(1);
                const rangeEnd = ((i+1) * (1/BUCKET_COUNT)).toFixed(1);
                const b = document.getElementById(`bucket-${i}`);
                b.innerHTML = `<span class="bucket-label">${rangeStart} - ${rangeEnd}</span>`;
            }
        }

        function addLog(msg, type='info') {
            const entry = document.createElement('div');
            entry.className = `log-entry log-${type}`;
            entry.innerText = `[${new Date().toLocaleTimeString()}] ${msg}`;
            logDiv.appendChild(entry);
            logDiv.scrollTop = logDiv.scrollHeight;
        }

        function sleep(ms) {
            return new Promise(resolve => setTimeout(resolve, ms));
        }

        async function startSort() {
            addLog('Starting Bucket Sort...', 'info');
            sortedArrayDiv.innerHTML = ''; // Clear output

            // 1. Scatter
            addLog('Step 1: Scattering elements to buckets...', 'warning');
            
            let bucketsData = Array.from({length: BUCKET_COUNT}, () => []);

            for(let i=0; i<arr.length; i++) {
                const num = arr[i];
                // Index = floor(n * bucket_count)
                let idx = Math.floor(num * BUCKET_COUNT);
                if(idx >= BUCKET_COUNT) idx = BUCKET_COUNT - 1; // Handle 1.0 if strictly included
                
                bucketsData[idx].push(num);
                
                // Visual Move
                const sourceEl = document.getElementById(`num-${i}`);
                sourceEl.classList.add('highlight');
                await sleep(DELAY/2);
                
                const clone = sourceEl.cloneNode(true);
                clone.style.display = 'block';
                clone.style.width = 'fit-content';
                clone.style.margin = '5px auto';
                clone.classList.remove('highlight');
                
                const bucketDiv = document.getElementById(`bucket-${idx}`);
                bucketDiv.appendChild(clone);
                
                sourceEl.style.opacity = '0.2';
                
                addLog(`Value ${num} -> Bucket ${idx}`);
                await sleep(DELAY/2);
            }

            // 2. Sort Buckets
            addLog('Step 2: Sorting individual buckets...', 'warning');
            for(let i=0; i<BUCKET_COUNT; i++) {
                if(bucketsData[i].length > 0) {
                    addLog(`Sorting Bucket ${i}...`);
                     const bucketDiv = document.getElementById(`bucket-${i}`);
                     bucketDiv.style.borderColor = 'var(--accent-color)';
                     await sleep(DELAY);
                     
                     // Logic sort
                     bucketsData[i].sort((a,b) => a-b);
                     
                     // Visual update
                     // Clear content except label
                     const label = bucketDiv.querySelector('.bucket-label');
                     bucketDiv.innerHTML = '';
                     bucketDiv.appendChild(label);
                     
                     for(let val of bucketsData[i]) {
                         const el = document.createElement('div');
                         el.className = 'num-block';
                         el.style.width = 'fit-content';
                         el.style.margin = '5px auto';
                         el.style.background = '#d1fae5'; // Light green
                         el.innerText = val.toFixed(2);
                         bucketDiv.appendChild(el);
                     }
                     
                     await sleep(DELAY);
                     bucketDiv.style.borderColor = '';
                }
            }

            // 3. Gather
            addLog('Step 3: Gathering sorted elements...', 'success');
            for(let i=0; i<BUCKET_COUNT; i++) {
                for(let val of bucketsData[i]) {
                    const el = document.createElement('div');
                    el.className = 'num-block';
                    el.innerText = val.toFixed(2);
                    el.classList.add('highlight');
                    sortedArrayDiv.appendChild(el);
                    await sleep(200);
                }
            }

            addLog('Bucket Sort Complete!', 'success');
        }

        // Init
        generateRandomArray();
    </script>
