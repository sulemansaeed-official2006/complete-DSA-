                <h3 style="color: var(--accent-color);">Controls</h3>
                <div class="controls">
                    <button onclick="generateRandomArray()" class="btn btn-primary">Generate Random Floats</button>
                    <button onclick="startSort()" class="btn btn-info">Start Bucket Sort</button>
                </div>
            </div>

            <div class="control-card log-panel">
                 <h3 style="color: var(--accent-color);">Execution Log</h3>
                 <div id="log" class="operation-log" style="height: 150px;"></div>
